<?php
//OPERADORES DE INCREMENTO  E DECREMENTO

$A = 10;
echo $A++; //incremento ocorre depois do echo
echo "<br/>";
echo $A;
echo "<br/>";

$A = 10;
echo ++$A; //incremento ocorre antes do echo
echo "<br/>";
echo $A;
echo "<br/>";

echo --$A; //decremento ocorre antes do echo
echo "<br/>";
echo $A;
echo "<br/>";

?>